using namespace std;
//при вызове функции без парметров по умолчанию, то должно при вызове функции передаваться столько же аргументов, 
//сколько перечислено параметров в скобочках в том же порядке.
//при использовании параметра по умолчанию, его можно пропускать при вызове функции

// параметры по умолчанию должны идти с правого конца списка параметров!!!

